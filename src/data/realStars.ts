// B-V 色指数から CSS 色へ変換
function bvToColor(bv: number): string {
  if (bv < -0.30) return '#9bb8ff'  // 青白 O型
  if (bv < -0.10) return '#aac4ff'  // 青白 B型
  if (bv <  0.20) return '#d0e0ff'  // 白   A/F型
  if (bv <  0.60) return '#fff8ee'  // 黄白 F/G型
  if (bv <  1.00) return '#ffe88a'  // 黄   G/K型
  if (bv <  1.50) return '#ffb347'  // 橙   K型
  return '#ff7733'                   // 赤橙 M型
}

// 等級から半径へ (実視等級が小さいほど大きい)
function magToRadius(vmag: number): number {
  if (vmag < 0)   return 4.0
  if (vmag < 1.0) return 3.2
  if (vmag < 2.0) return 2.5
  if (vmag < 3.0) return 1.8
  if (vmag < 4.0) return 1.3
  if (vmag < 5.0) return 0.9
  return 0.6
}

// シード付き乱数
function seededRand(seed: number) {
  let s = seed
  return () => {
    s = (s * 1664525 + 1013904223) & 0xffffffff
    return (s >>> 0) / 0xffffffff
  }
}

export const CATALOG_WIDTH  = 8640   // 360 × 24 (px per degree)
export const CATALOG_HEIGHT = 4320   // 180 × 24

let cachedStars: StarData[] | null = null

export function buildCatalogStars(): StarData[] {
  if (cachedStars) return cachedStars

  const stars: StarData[] = []
  const rand = seededRand(20240601)
  const seen = new Set<string>()   // 重複排除

  // ① BSC 実星データ
  for (let i = 0; i < BSC_DATA.length; i++) {
    const [ra, dec, vmag, bv] = BSC_DATA[i]
    const x = (ra / 24) * CATALOG_WIDTH
    const y = ((90 - dec) / 180) * CATALOG_HEIGHT
    const key = `${Math.round(x)},${Math.round(y)}`
    if (seen.has(key)) continue
    seen.add(key)

    stars.push({
      id: `bsc-${i}`,
      x,
      y,
      size: magToRadius(vmag),
      twinkle: rand() * 4,
      color: bvToColor(bv),
    })
  }

  // ② 背景の淡い星をランダムに追加 (等級 5〜8 相当)
  const BG_COUNT = 5000
  for (let i = 0; i < BG_COUNT; i++) {
    const x = rand() * CATALOG_WIDTH
    const y = rand() * CATALOG_HEIGHT
    const magOffset = rand()  // 0〜1 → 等級 5〜8
    const size = magOffset < 0.3 ? 0.9 : magOffset < 0.7 ? 0.6 : 0.4
    const bv = rand() * 1.8 - 0.3
    stars.push({
      id: `bg-${i}`,
      x,
      y,
      size,
      twinkle: rand() * 5,
      color: bvToColor(bv),
    })
  }

  cachedStars = stars
  return stars
}
